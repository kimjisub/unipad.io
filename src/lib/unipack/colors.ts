export function argbToRgba(argb: number): string {
  const a = ((argb >>> 24) & 0xff) / 255;
  const r = (argb >>> 16) & 0xff;
  const g = (argb >>> 8) & 0xff;
  const b = argb & 0xff;
  return `rgba(${r},${g},${b},${a.toFixed(2)})`;
}

export function velocityToColor(velocity: number): string {
  if (velocity < 0 || velocity >= LAUNCHPAD_ARGB.length) return 'transparent';
  return argbToRgba(LAUNCHPAD_ARGB[velocity]);
}
